    <p style="font-family: 'Ubuntu', sans-serif; text-align: justify;">Commerce Olympiad is held every year by Olympiad Foundation (OF). The students who are studying in
11th and 12th standard in Commerce stream can appear for this test.<br><br>
It is a Non- Government organization whose main objective is to motivate and encourage the talented
students of commerce stream to achieve higher goals in their career. The aim of organization is to
support and provide better opportunities to the students of commerce. Following these objectives our
NGO conducts Commerce Olympiad exams so as to enhance the knowledge of students and to boost
their excellence through such tests. The tests are objective in nature and are conducted only in English
Medium. Our main motive is to extensively trigger the interests of students pursuing commerce stream
and regulate their efforts and excellence towards achieving bright careers in ICAI, ICMAI, ICSI, CAT (IIM)
etc.<br><br>
The sole aim of Commerce Olympiad to provide an opportunity to the students to crack the exam of
ICSI, ICAI, ICMAI, IIM, etc. The syllabus of Commerce Olympiad is totally based on such pattern of exam
ICAI, ICMAI, ICSI, CAT (IIM) etc. So the students who participate in such exams conducted by Commerce
Olympiad get beneﬁtted. The Olympiad foundation also oﬀers scholarship and wide range of prizes for
those who crack the exam like CA, ICWA, CS, IIM, etc.</p>
