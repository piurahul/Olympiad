    <p style="font-family: 'Ubuntu', sans-serif; text-align: justify;">Science is the study of the nature and behavior of natural things and knowledge that we obtain
about them. The mechanism of Science is quite astonishing that makes one wonder about the way it
works. Our NGO provides platform for the lovers of science or those who want to pursue career in
science . The International Junior Science Olympiad is a science competition held annually for the
students who are with in the age of 15 years. The test covers all stream of science including physics,
chemistry , biology., etc.<br><br>
Science Olympiad was conducted at Jakarta ( Indonesia) in 2004 .Nearly more than 60 countries
participated in that competition. The participating countries sent delegations of three to six students
with not more than three team leaders and observers. The competition was conducted in three stages
by three tests. Every test was lasted for a time limit of two to three hours. The theoretical portion was
also covered up in two tests. There were multiple choice questions that were comprised of 30 questions
followed by the second test which was subjective in NEET, AIIMS and IIT.<br><br>
The test conducted by Science Olympiad is totally based on the syllabus of NEET, AIIMS, IIT,JEE,
so the students who crack such type of test get desired fruits. They are then easily able to qualify such
type of exams. The Olympiad foundation&#39;s main aim is to provide a platform for such students who
wants to qualify exam like NEET, IIT etc. This organization supports such students and nurtures their
talent. It also awards them prizes and scholarships.</p>
