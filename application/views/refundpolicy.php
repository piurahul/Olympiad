				<h1 style="text-align:center;" >FEE REFUND POLICY</h1><hr>
<p><strong>In case of New Admission</strong></p>
<ol>
<li>In case where a student withdraws admission/communicates his/her intention not to attend the school one month before the date of scheduled commencement of classes, Full Fee and security paid to the school will be refunded after deducting Rs 15000/- as administrative charges.</li>
</ol>
<strong><u>Note :</u></strong><br><br>
<p>  1. Fees includes –</p>
<ol>
<li>Admission Fee</li>
<li>Security Charges</li>
<li>1st Quarter Fees</li>
<li>Transport Charges</li>
<li>Annual Charges and many more .....</li>
</ol>
<p>2. In the case where a student withdraws admission or communicates his /her intention not to attend the school less than a month before the date of the scheduled commencement of classes, the following Fee Amount (If paid) will be refunded.</p>
<ol>
<li>Security Amount</li>
<li>1st Quarter Fee</li>
<li>Transport Fee</li>
<li>Annual Charges (as applicable)</li>
</ol>

<p><strong> Note: &nbsp; Admission fee will not be refunded in the above case</strong></p>
<p>
3. In case where a student withdraws admission/communicates his /her intention not to attend the school after the date of commencement of classes, then the Rules of Fee Refund governing existing students become applicable in mutatis mutandis, all unpaid fees must be received before TC is issued to the concerned student.
</p>
<p><strong> Note: &nbsp; Security amount, if any, shall be refunded without interest, after adjustment of dues, if any, on withdrawal from the school. Security deposit is adjustable against damage to, or loss of –</strong></p>
<ol>
<li>Library books</li>
<li>Laboratory equipment</li>
<li>Technology facilities &amp;</li>
<li>Other equipment or assets of the school</li>
</ol>
<p><strong>In case of Existing Student withdrawing in the mid /end of the session</strong></p>
<p>In order to withdraw their ward, parents need to submit their application in the prescribed format at least a month before the date of withdrawal.</p>
<p><strong>Fee amount to be refunded as mentioned below:-</strong></p>
<ol>
<li>Student who has paid the *full quarter fee needs to be charged only up to the end of the month in which the student has last attended classes while the remaining proportionate fee for that quarter will be refunded.</li>
<li>Student who has not paid the * full quarter fees, needs to pay till the end of the month that he/she has last attended the classes in.</li>
<li>Transport Fee needs to be refunded /recovered proportionately on similar pattern as per point no. ( i) &amp; (ii) mentioned above.</li>
<li>In case of Annual Charges /Payments the fee needs to be refunded /recovered proportionately up to end of the quarter in which student has last attended the classes.</li>
</ol>
<p><strong> Note: &nbsp;  Annual charges /Payment need to be worked out quarterly for the purpose of point no. (iv) Above</strong></p>
<ol>
<li>Security amount, if any needs to refunded without interest after adjustment of dues, if any, on completion of the student’s studies. Security deposit is adjustable against damage to, or loss of, library books, laboratory equipment, technology facilities and other equipment or assets of the school. All unpaid fees must be paid before TC is issued to the student.</li>
<li>In case of Resource fee, it will be refunded only when school kit (Books &amp; Note books or Bag etc.) has not been given to the student.</li>
</ol>
